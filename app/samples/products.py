def _prepared_product(product: dict) -> dict:
    prepared = deepcopy(product)
    prepared["source_url"] = f"/api/deals/products/{prepared['id']}/page"
    prepared["image_url"] = _sample_product_image_url(
        prepared.get("category"),
        prepared.get("color"),
        prepared.get("brand"),
        prepared.get("title"),
    )
    return prepared


def _sample_product_image_url(category: str | None, color: str | None, brand: str | None, title: str | None) -> str:
    query = {
        "category": category or "Product",
        "color": color or "Neutral",
        "brand": brand or "",
        "title": title or "",
    }
    return "/api/catalog-images/product.svg?" + "&".join(
        f"{key}={quote_plus(value)}" for key, value in query.items()
    )
